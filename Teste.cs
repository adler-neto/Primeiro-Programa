namespace Teste
{
    public class Contas
    {
        public int Soma(int a, int b)
        {
            return a + b;
        }

        public int Subtracao(int a, int b)
        {
            return a - b;
        }

        public int Divisao(int a, int b)
        {
            return a / b;
        }

        public int Multplicacao(int a, int b)
        {
            return a * b;
        }
    }
}